/**
 * Domain stub generator (Epic 13 / GAP-1503 / phase 81).
 * Creates SQL + list page + API route with workspace_id tenancy TODOs.
 * Stubs only — not a fake CRM.
 */
import fs from "fs";
import path from "path";
import { die, ensureDir, entityNames, write } from "./shared.mjs";

export function generateDomainStub(appRoot, entityRaw) {
  const { kebab, pascal, camel, table } = entityNames(entityRaw);
  const migrationsDir = path.join(appRoot, "src", "domain", "migrations", "versions");
  ensureDir(migrationsDir);

  const existing = fs
    .readdirSync(migrationsDir)
    .filter((f) => /^\d+_/.test(f) && f.endsWith(".sql"));
  const nextNum = String(existing.length + 1).padStart(3, "0");
  const sqlName = `${nextNum}_${table}.sql`;

  write(
    path.join(migrationsDir, sqlName),
    `-- Domain stub for "${pascal}" (product-owned).
-- HISTORY: generated by @llanesleonardo/create-saas --entity ${kebab}
-- TODO: apply via your migrate path; add indexes as needed.
-- TODO: enforce workspace_id scoping with chassis tenancy (never query without workspace).

create table if not exists ${table} (
  id uuid primary key default gen_random_uuid(),
  workspace_id uuid not null, -- TODO: references workspaces(id) on delete cascade
  title text not null default '',
  created_at timestamptz not null default now(),
  updated_at timestamptz
);

create index if not exists ${table}_workspace_id_idx on ${table} (workspace_id);
`,
  );

  write(
    path.join(appRoot, "src", "app", "domain", kebab, "page.tsx"),
    `/**
 * ${pascal} list page — empty domain stub (not a full CRM).
 * TODO: load rows for the active workspace only (workspace_id scoping).
 */
export default function ${pascal}ListPage() {
  return (
    <main style={{ padding: 24, fontFamily: "system-ui, sans-serif" }}>
      <h1>${pascal}</h1>
      <p>
        Stub list for <code>${table}</code>. Wire data with chassis tenancy —
        always filter by <code>workspace_id</code>.
      </p>
      <ul>
        <li>No rows yet — implement fetch in a server component or client loader.</li>
      </ul>
    </main>
  );
}
`,
  );

  write(
    path.join(appRoot, "src", "app", "api", "domain", kebab, "route.ts"),
    `import { NextResponse } from "next/server";

/**
 * ${pascal} API stub — product-owned, not chassis.
 * TODO: resolve active workspace from session cookie / shell tenancy helpers.
 * TODO: scope every query with workspace_id (never return cross-tenant rows).
 */
export async function GET() {
  // const workspaceId = ...;
  const items: Array<{ id: string; title: string }> = [];
  return NextResponse.json({ ${camel}: items, stub: true });
}

export async function POST(request: Request) {
  const body = (await request.json().catch(() => ({}))) as { title?: string };
  // TODO: insert into ${table} with workspace_id
  return NextResponse.json(
    {
      id: "stub",
      title: body.title ?? "",
      message: "Stub only — persist with workspace_id scoping before production.",
    },
    { status: 501 },
  );
}
`,
  );

  const readme = path.join(appRoot, "src", "domain", "README.md");
  if (fs.existsSync(readme)) {
    const prev = fs.readFileSync(readme, "utf8");
    if (!prev.includes(`domain/${kebab}`)) {
      fs.appendFileSync(
        readme,
        `\n## Stub: ${pascal}\n\n- Migration: \`migrations/versions/${sqlName}\`\n- UI: \`/domain/${kebab}\`\n- API: \`/api/domain/${kebab}\`\n`,
        "utf8",
      );
    }
  }

  console.log(`domain stub: ${pascal} → SQL ${sqlName}, /domain/${kebab}, /api/domain/${kebab}`);
  return { kebab, pascal, table, sqlName };
}

export function assertAppRoot(dir) {
  const root = path.resolve(dir);
  if (!fs.existsSync(path.join(root, "package.json"))) {
    die(`not an app directory (missing package.json): ${root}`);
  }
  return root;
}
